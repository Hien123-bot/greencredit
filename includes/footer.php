<script>
    document.addEventListener("DOMContentLoaded", function() {
        <?php if (isset($_GET['success'])): ?>
            <?php if ($_GET['success'] === 'points_added'): ?>
                showToast('Quét mã thành công! Đã cộng +<?= isset($_GET['amount']) ? (int)$_GET['amount'] : 0 ?> PTS', 'success');
            <?php else: ?>
                showToast('<?= htmlspecialchars($_GET['success']) ?>', 'success');
            <?php endif; ?>
        <?php endif; ?>

        <?php if (isset($_GET['error'])): ?>
            <?php if ($_GET['error'] === 'invalid_qr'): ?>
                showToast('Mã QR không hợp lệ hoặc không có trong hệ thống!', 'error');
            <?php elseif ($_GET['error'] === 'qr_already_used'): ?>
                showToast('Mã QR này đã được sử dụng trước đó!', 'error');
            <?php else: ?>
                showToast('<?= htmlspecialchars($_GET['error']) ?>', 'error');
            <?php endif; ?>
        <?php endif; ?>
        
        // Clean URL after showing toast
        if (window.history.replaceState) {
            const url = new URL(window.location);
            url.searchParams.delete('success');
            url.searchParams.delete('error');
            url.searchParams.delete('amount');
            window.history.replaceState({path: url.href}, '', url.href);
        }
    });
</script>
